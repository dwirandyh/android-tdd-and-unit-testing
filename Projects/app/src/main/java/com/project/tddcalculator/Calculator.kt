package com.project.tddcalculator

class Calculator {

    fun add(a: Int, b: Int): Int {
        return a + b
    }

    fun subtraction(a: Int, b: Int): Int {
        return a * b
    }

    fun multiplication(a: Int, b: Int) : Int {
        return a * b
    }

    fun divide(a: Int, b: Int): Int {
        return a/b
    }
}